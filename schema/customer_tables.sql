-- Main Customers table created from .csv original database

CREATE TABLE customer_data (
    customer_id SERIAL PRIMARY KEY,
    first_name TEXT,
    last_name TEXT,
    age INTEGER,
    gender TEXT,
    address TEXT,
    city TEXT,
    contact_number TEXT,
    email TEXT,
    account_type TEXT,
    account_balance FLOAT,
    date_of_account_opening DATE,
    last_transaction_date DATE,
    transaction_id INTEGER,
    transaction_date DATE,
    transaction_type TEXT,
    transaction_amount FLOAT,
    account_balance_after_transaction FLOAT,
    branch_id INTEGER,
    loan_id INTEGER,
    loan_amount FLOAT,
    loan_type TEXT,
    interest_rate FLOAT,
    loan_term INTEGER,
    approval_rejection_date DATE,
    loan_status TEXT,
    card_id INTEGER,
    card_type TEXT,
    credit_limit FLOAT,
    credit_card_balance FLOAT,
    minimum_payment_due FLOAT,
    payment_due_date DATE,
    last_credit_card_payment_date DATE,
    rewards_points INTEGER,
    feedback_id INTEGER,
    feedback_date DATE,
    feedback_type TEXT,
    resolution_status TEXT,
    resolution_date DATE,
    anomaly INTEGER
);